# Boolean Operators compare two conditional statements
# or negate a conditional statement from true to fales or from false to true
#
# Operator
# and True if both statements are true
#  or True if at least one statement is true
# not opposite of the statements true value

b = 2
c = "Andre"
print(f'What is the result of b ==  2 and c == "Andre"? {b == 2 and c == "Andre"}')
print(f'What is the result of b < 2 and c == "Andre"? {b < 2 and c == "Andre"}')
print(f'How about b < 2 or c == "Andre"? {b < 2 or c == "Andre"}')
print(f'What is the result of not b == 2? {not b == 2}')