##Conditional operators
#
# Operator
# == Equal to
# > Greater Than
# >= Greater than or equal to
# < Less than
# <= Less than or equal to
# != Not equal to
#

b = 2
print(f'Is b equal to 2? {b == 2}')
print(f'Is b equal to 3? {b == 3}')
print()
c = "Andre"
print(f'Is c equal to Andre? {c == "Andre"}')
print(f'Is c equal to "Jose"? {c == "Jose"}')